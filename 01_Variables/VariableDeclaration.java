public class VariableDeclaration {
    public static void main(String[] args) {

        int age;
        double salary;
        char grade;
        String name;

        age = 20;
        salary = 25000.50;
        grade = 'A';
        name = "Aravind";

        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Salary: " + salary);
        System.out.println("Grade: " + grade);
}}